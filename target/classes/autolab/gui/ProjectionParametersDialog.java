    public Projection getModifiedProjection(){
        if(isModified()){
            Projection prj = (Projection)projection.clone();
            if(prj instanceof ConicProjection){
                ConicProjection cprj = (ConicProjection)prj;
                cprj.setProjectionLatitudeDegrees(Double.valueOf(latitudeField.getText()));
                cprj.setProjectionLongitudeDegrees(Double.valueOf(meridianField.getText()));
                cprj.setProjectionLatitude1Degrees(Double.valueOf(parallelField1.getText()));
                cprj.setProjectionLatitude2Degrees(Double.valueOf(parallelField2.getText()));
                cprj.setScaleFactor(Double.valueOf(scaleField.getText()));
                cprj.setEllipsoid(Ellipsoid.SPHERE);
                cprj.initialize();
                return cprj;
            } else {
                return null;
            }
        } else return null;
    }
    
    public boolean isModified(){
        return true;
    }
